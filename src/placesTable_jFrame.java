    private void Update_table(){
        
        try{
            String sql = "select * from Places";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            Table_Places.setModel(DbUtils.resultSetToTableModel(rs));
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try{
                rs.close();
                pst.close();
            } catch (Exception e){
                
            }
        }
        
    }
    
