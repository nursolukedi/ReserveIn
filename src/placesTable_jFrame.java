    private void Table_PlacesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_PlacesMouseClicked

        try{
            int row = Table_Places.getSelectedRow();
            String Table_click = (Table_Places.getModel().getValueAt(row, 0).toString());

            String sql = "select * from Places where Placeid='" + Table_click + "' ";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            if(rs.next()){

                String add1 = rs.getString("Placeid");
                txt_placeid.setText(add1);

                String add2 = rs.getString("Name");
                txt_name.setText(add2);

                String add3 = rs.getString("Adress");
                txt_adress.setText(add3);

                String add4 = rs.getString("Type");
                txt_type.setText(add4);

                String add5 = rs.getString("Grade");
                txt_grade.setText(add5);
            }

        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally {
            try{
                rs.close();
                pst.close();
            } catch (Exception e){
                
            }
        }
    }//GEN-LAST:event_Table_PlacesMouseClicked

